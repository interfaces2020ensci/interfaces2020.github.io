$(document).ready(function(){

  var max = 14;
  var i = 0;

    $(".image-wrapper").on("mousemove", function() {
       $(".image-wrapper").addClass("no-mask");
    })

  $('body').on('click', function(){
      console.log("nouvelle image");
    var imagePath = 'images/'+images[i].IMAGE + '.jpg';
    var imageX = images[i].x;
    var imageY = images[i].y;
    var imageScale = images[i].taille;
    var imageDate = images[i].ANNEE;
      var imageMarque = images[i].MARQUE;
    $('.cartel').html('<p class="date">'+imageDate+'</p>'+'<p class="marque">'+imageMarque+'</p>');

    $('.woman-image').css({
        'top': imageY,
        'left': imageX,
        'transform': 'scale('+imageScale+')',
    });
    $('.woman-image img').attr('src', imagePath);
    if(i<max){
      i++ ;
    }
    else{
      i = 0;
    }

       $(".image-wrapper").removeClass("no-mask");

  });

    $(document).on('mousemove', function(e){
        $('.cartel').css({
           left:  e.pageX,
           top:   e.pageY
        });
    });


});

let valeur_zoom = 1;

document.addEventListener("mousewheel", getScroll);
document.addEventListener("DOMMouseScroll", getScroll);

function getScroll(e) {
  console.log('souris a tourné : ' + valeur_zoom);
  let scroll = (e.wheelDelta || -e.detail);
  valeur_zoom = valeur_zoom + scroll / 500;


  valeur_zoom = Math.min( 4, valeur_zoom);
  valeur_zoom = Math.max( .06, valeur_zoom);

  document.querySelector(".content").style.transform = "scale(" + valeur_zoom + ")";

  document.querySelector(".grille").style.backgroundSize = (valeur_zoom * 37.1) + "px " + (valeur_zoom * 37.1) + "px"

}

